    private void btnFinishedOrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishedOrderActionPerformed
      double countTime=Double.valueOf(txttime.getText()) ;
        
        
       String Oid =txtID.getText();
       String name =txtname.getText();
       String TelephoneNo =txtPno.getText();
       int qty = Integer.parseInt(txtQty.getText());
       String TON =txtTpn.getText();
       double time=countTime;
       
       if(countTime>60){
           time=countTime/60;
       }
       String Cid= txtCID.getText();
       Date date=Date.valueOf(localDate);
       Time transtime=Time.valueOf(localtime);
       
          try {
           
            Connection con=DBConnection.DBConnection.getnstance().getConnection();
            PreparedStatement ps=con.prepareStatement("INSERT INTO OrderDetails VALUES(?,?,?,?,?,?,?,?,?)");
            ps.setString(1, Oid);
            ps.setString(2, Cid);
            ps.setString(3, TON);
            ps.setString(4, name);
            ps.setString(5, TelephoneNo);
            ps.setInt(6, qty);
            ps.setDouble(7, time);
            ps.setObject(8, date);
            ps.setObject(9, transtime);
            int conf=ps.executeUpdate();
            
            if(conf>0){
                JOptionPane.showMessageDialog(rootPane, "Order Finished Successfully");
                SetTextArea();
                clearAllText();
                
            }else {
                JOptionPane.showMessageDialog(rootPane, "Error Try again");
            }
            
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(ChefWindow.class.getName()).log(Level.SEVERE, null, ex);
        }
        
       
        
    }//GEN-LAST:event_btnFinishedOrderActionPerformed

