    public void SartTime(){
    
          
        Timer timer = new Timer(); //new timer
        counter = 0; //setting the counter to 10 sec
        TimerTask task = new TimerTask() {         
            public void run() {                
                txttime.setText(Integer.toString(counter)); //the timer lable to counter.
                counter ++;
                
                if (counter == -1){
                    timer.cancel();                                       
                } else if(isIt){
                    timer.cancel();
                    isIt = false;
                    
                }
            }
        };
    timer.scheduleAtFixedRate(task, 1000, 1000); // =  timer.scheduleAtFixedRate(task, delay, period);
        
    }
    
    
